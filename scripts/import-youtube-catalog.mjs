#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import readline from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";
import { youtubeImportSources } from "./youtube-import.config.mjs";
import { enrichAnimeMetadata } from "./lib/anime-metadata.mjs";
import {
  createAnimeFromPlaylist,
  fetchChannelPlaylists,
  fetchPlaylistItems,
  fetchVideoDetails,
  isSkippedPlaylistItem,
  isUsablePlaylist,
  loadEnv,
  resolveChannel,
  slugify
} from "./lib/youtube-api.mjs";

const DEFAULT_OUTPUT = "data/generated/anime.generated.ts";
const DEFAULT_JSON_OUTPUT = "data/generated/youtube-catalog.json";

function parseArgs(argv) {
  const args = {
    source: "all",
    force: false,
    dryRun: false,
    interactive: false,
    listPlaylists: false,
    onlyPlaylist: undefined,
    enrichMetadata: false,
    metadataThreshold: undefined,
    limitPlaylists: undefined,
    output: DEFAULT_OUTPUT,
    jsonOutput: DEFAULT_JSON_OUTPUT
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    const next = argv[index + 1];

    if (arg === "--source") {
      args.source = next ?? "all";
      index += 1;
    } else if (arg === "--force") {
      args.force = true;
    } else if (arg === "--dry-run") {
      args.dryRun = true;
    } else if (arg === "--interactive") {
      args.interactive = true;
    } else if (arg === "--list-playlists") {
      args.listPlaylists = true;
    } else if (arg === "--only-playlist") {
      args.onlyPlaylist = next;
      index += 1;
    } else if (arg === "--enrich-metadata") {
      args.enrichMetadata = true;
    } else if (arg === "--metadata-threshold") {
      args.metadataThreshold = Number(next);
      index += 1;
    } else if (arg === "--limit-playlists") {
      args.limitPlaylists = Number(next);
      index += 1;
    } else if (arg === "--output") {
      args.output = next ?? DEFAULT_OUTPUT;
      index += 1;
    } else if (arg === "--json-output") {
      args.jsonOutput = next ?? DEFAULT_JSON_OUTPUT;
      index += 1;
    } else if (arg === "--help" || arg === "-h") {
      printHelp();
      process.exit(0);
    } else {
      throw new Error(`Unknown CLI flag: ${arg}`);
    }
  }

  if (
    args.limitPlaylists !== undefined &&
    (!Number.isInteger(args.limitPlaylists) || args.limitPlaylists < 1)
  ) {
    throw new Error("--limit-playlists must be a positive integer.");
  }

  if (
    args.metadataThreshold !== undefined &&
    (!Number.isFinite(args.metadataThreshold) ||
      args.metadataThreshold < 0 ||
      args.metadataThreshold > 1)
  ) {
    throw new Error("--metadata-threshold must be a number from 0 to 1.");
  }

  return args;
}

function printHelp() {
  console.log(`
Import official YouTube playlists into local static data.

Examples:
  node scripts/import-youtube-catalog.mjs --source all
  node scripts/import-youtube-catalog.mjs --source muse-indonesia --limit-playlists 5
  node scripts/import-youtube-catalog.mjs --source ani-one-indonesia --force

Flags:
  --source <all|source-key>       Source to import
  --force                         Reserved for explicit overwrite workflows
  --dry-run                       Fetch and summarize without writing files
  --interactive                   Choose playlists before importing videos
  --list-playlists                Print matching playlists without importing videos
  --only-playlist <playlist-id>   Import only one playlist ID
  --enrich-metadata               Enrich anime posters, genres, banners, and synopsis from AniList
  --metadata-threshold <0-1>      Confidence threshold for auto-applying metadata
  --limit-playlists <number>      Limit playlists per source
  --output <path>                 TypeScript output path
  --json-output <path>            JSON output path
`);
}

function selectSources(sourceKey) {
  if (sourceKey === "all") {
    return youtubeImportSources;
  }

  const source = youtubeImportSources.find((item) => item.key === sourceKey);

  if (!source) {
    const available = youtubeImportSources.map((item) => item.key).join(", ");
    throw new Error(`Unknown source "${sourceKey}". Available sources: all, ${available}`);
  }

  return [source];
}

function writeJson(filePath, data) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

function writeTypeScript(filePath, data) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  const body = JSON.stringify(data, null, 2);
  const content = `import type { Anime } from "@/types/anime";

// This file is generated by scripts/import-youtube-catalog.mjs.
// Do not edit by hand. Run npm run youtube:import to refresh it.
export const generatedAnimeData = ${body} satisfies Anime[];
`;

  fs.writeFileSync(filePath, content, "utf8");
}

function createEmptyStats() {
  return {
    sourcesImported: 0,
    playlistsFound: 0,
    playlistsImported: 0,
    videosFound: 0,
    videosImported: 0,
    privateDeletedSkipped: 0,
    playlistsSkipped: 0,
    nonEmbeddableVideos: 0,
    duplicateAnimeTitles: 0,
    duplicatePlaylistIds: 0,
    duplicateVideoIds: 0,
    duplicateEpisodeTitles: 0,
    duplicateReport: [],
    metadataEnriched: 0,
    metadataNeedsReview: 0,
    sourceSummaries: [],
    outputFilesWritten: []
  };
}

function mergeStats(target, source) {
  for (const key of Object.keys(target)) {
    if (Array.isArray(target[key])) {
      target[key].push(...source[key]);
    } else {
      target[key] += source[key];
    }
  }
}

async function choosePlaylistsInteractively(source, playlists) {
  if (!process.stdin.isTTY) {
    throw new Error("--interactive requires an interactive terminal.");
  }

  const rl = readline.createInterface({ input, output });
  const selected = [];

  try {
    for (const playlist of playlists) {
      const title = playlist.snippet?.title ?? playlist.id;
      const answer = await rl.question(
        `Import "${title}" from ${source.name}? [y/N] `
      );

      if (answer.trim().toLowerCase().startsWith("y")) {
        selected.push(playlist);
      }
    }
  } finally {
    rl.close();
  }

  return selected;
}

function printPlaylistList(source, playlists) {
  console.log(`\nAvailable playlists for ${source.name}`);
  console.log("----------------------------------------");

  for (const playlist of playlists) {
    console.log(
      `${playlist.id}\t${playlist.contentDetails?.itemCount ?? 0} videos\t${playlist.snippet?.title ?? "Untitled"}`
    );
  }
}

function addToGroup(groups, key, label) {
  if (!key) {
    return;
  }

  groups.set(key, [...(groups.get(key) ?? []), label]);
}

function addDuplicateReportLines(lines, label, groups) {
  for (const [key, items] of groups.entries()) {
    if (items.length > 1) {
      lines.push(`${label}: ${key} (${items.length} matches)`);
    }
  }
}

function buildDuplicateReport(animeList) {
  const animeTitles = new Map();
  const playlistIds = new Map();
  const videoIds = new Map();
  const episodeTitles = new Map();
  const lines = [];

  for (const anime of animeList) {
    addToGroup(animeTitles, slugify(anime.title), anime.title);
    addToGroup(playlistIds, anime.youtubePlaylistId ?? anime.playlistId, anime.title);

    for (const episode of anime.episodes) {
      const episodeTitleKey = slugify(episode.title);

      if (episodeTitleKey && !/^episode-\d+$/.test(episodeTitleKey)) {
        addToGroup(episodeTitles, episodeTitleKey, `${anime.title} / ${episode.title}`);
      }

      addToGroup(videoIds, episode.youtubeVideoId, `${anime.title} / ${episode.title}`);
    }
  }

  addDuplicateReportLines(lines, "duplicate anime title", animeTitles);
  addDuplicateReportLines(lines, "duplicate playlist ID", playlistIds);
  addDuplicateReportLines(lines, "duplicate video ID", videoIds);
  addDuplicateReportLines(lines, "duplicate episode title", episodeTitles);

  return {
    lines,
    counts: {
      duplicateAnimeTitles: Array.from(animeTitles.values()).filter((items) => items.length > 1).length,
      duplicatePlaylistIds: Array.from(playlistIds.values()).filter((items) => items.length > 1).length,
      duplicateVideoIds: Array.from(videoIds.values()).filter((items) => items.length > 1).length,
      duplicateEpisodeTitles: Array.from(episodeTitles.values()).filter((items) => items.length > 1).length
    }
  };
}

async function importSource(source, options) {
  const stats = createEmptyStats();
  const generatedAnime = [];

  console.log(`\nImporting source: ${source.name} (${source.key})`);
  const channel = await resolveChannel(source);
  console.log(`Resolved channel: ${channel.snippet?.title ?? source.name} (${channel.id})`);

  const playlists = await fetchChannelPlaylists(channel.id);
  const matchingPlaylists = playlists.filter((playlist) => isUsablePlaylist(source, playlist));
  let usablePlaylists = matchingPlaylists
    .filter((playlist) => !options.onlyPlaylist || playlist.id === options.onlyPlaylist)
    .slice(0, options.limitPlaylists ?? playlists.length);

  stats.sourcesImported = 1;
  stats.playlistsFound = playlists.length;
  stats.playlistsSkipped = playlists.length - usablePlaylists.length;

  if (options.listPlaylists) {
    printPlaylistList(source, usablePlaylists);
    stats.sourceSummaries.push({
      sourceKey: source.key,
      sourceName: source.name,
      playlistsFound: stats.playlistsFound,
      playlistsImported: 0,
      playlistsSkipped: stats.playlistsSkipped
    });
    return {
      stats,
      generatedAnime
    };
  }

  if (options.interactive) {
    usablePlaylists = await choosePlaylistsInteractively(source, usablePlaylists);
    stats.playlistsSkipped = playlists.length - usablePlaylists.length;
  }

  for (const playlist of usablePlaylists) {
    const title = playlist.snippet?.title ?? playlist.id;
    console.log(`- Playlist: ${title}`);

    const playlistItems = await fetchPlaylistItems(playlist.id);
    const skippedBeforeDetails = playlistItems.filter(isSkippedPlaylistItem).length;
    const candidateItems = playlistItems.filter((item) => !isSkippedPlaylistItem(item));
    const videoIds = candidateItems
      .map((item) => item.contentDetails?.videoId)
      .filter(Boolean);
    const videoDetails = await fetchVideoDetails(videoIds);
    const videoDetailsMap = new Map(videoDetails.map((video) => [video.id, video]));
    const missingDetails = videoIds.filter((videoId) => !videoDetailsMap.has(videoId)).length;
    let anime = createAnimeFromPlaylist(
      source,
      playlist,
      playlistItems,
      videoDetailsMap
    );

    stats.videosFound += playlistItems.length;
    stats.privateDeletedSkipped += skippedBeforeDetails + missingDetails;

    if (!anime) {
      console.log("  Skipped: no usable public videos.");
      continue;
    }

    if (options.enrichMetadata) {
      try {
        anime = await enrichAnimeMetadata(anime, {
          confidenceThreshold: options.metadataThreshold
        });

        if (anime.metadataNeedsReview) {
          stats.metadataNeedsReview += 1;
          console.log(
            `  Metadata needs review (${anime.metadataConfidence ?? 0} confidence).`
          );
        } else {
          stats.metadataEnriched += 1;
          console.log(
            `  Metadata enriched from ${anime.metadataSource} (${anime.metadataConfidence} confidence).`
          );
        }
      } catch (error) {
        stats.metadataNeedsReview += 1;
        anime = {
          ...anime,
          metadataNeedsReview: true,
          metadataConfidence: 0,
          metadataCandidates: []
        };
        console.log(`  Metadata skipped: ${error.message}`);
      }
    }

    stats.playlistsImported += 1;
    stats.videosImported += anime.episodes.length;
    stats.nonEmbeddableVideos += anime.episodes.filter(
      (episode) => episode.embeddable === false
    ).length;
    generatedAnime.push(anime);
  }

  stats.sourceSummaries.push({
    sourceKey: source.key,
    sourceName: source.name,
    playlistsFound: stats.playlistsFound,
    playlistsImported: stats.playlistsImported,
    playlistsSkipped: stats.playlistsSkipped
  });

  return {
    stats,
    generatedAnime
  };
}

function printSummary(stats, options) {
  console.log("\nYouTube import summary");
  console.log("----------------------");
  console.log(`Sources imported: ${stats.sourcesImported}`);
  console.log(`Playlists found: ${stats.playlistsFound}`);
  console.log(`Playlists imported: ${stats.playlistsImported}`);
  console.log(`Skipped playlists: ${stats.playlistsSkipped}`);
  console.log(`Videos found: ${stats.videosFound}`);
  console.log(`Videos imported: ${stats.videosImported}`);
  console.log(`Private/deleted skipped: ${stats.privateDeletedSkipped}`);
  console.log(`Non-embeddable videos detected: ${stats.nonEmbeddableVideos}`);
  console.log(`Duplicate anime title groups: ${stats.duplicateAnimeTitles}`);
  console.log(`Duplicate playlist ID groups: ${stats.duplicatePlaylistIds}`);
  console.log(`Duplicate video ID groups: ${stats.duplicateVideoIds}`);
  console.log(`Duplicate episode title groups: ${stats.duplicateEpisodeTitles}`);

  if (options.enrichMetadata) {
    console.log(`Metadata enriched: ${stats.metadataEnriched}`);
    console.log(`Metadata needs review: ${stats.metadataNeedsReview}`);
  }

  if (stats.sourceSummaries.length > 0) {
    console.log("Source summary:");
    for (const source of stats.sourceSummaries) {
      console.log(
        `- ${source.sourceName}: ${source.playlistsImported} imported, ${source.playlistsSkipped} skipped, ${source.playlistsFound} found`
      );
    }
  }

  if (stats.duplicateReport.length > 0) {
    console.log("Duplicate report:");
    for (const item of stats.duplicateReport.slice(0, 20)) {
      console.log(`- ${item}`);
    }

    if (stats.duplicateReport.length > 20) {
      console.log(`- ...and ${stats.duplicateReport.length - 20} more`);
    }
  }

  if (options.dryRun || options.listPlaylists) {
    console.log("Output files written: no files written");
  } else {
    console.log(`Output files written: ${stats.outputFilesWritten.join(", ")}`);
  }
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const selectedSources = selectSources(options.source);
  const combinedStats = createEmptyStats();
  const combinedAnime = [];

  loadEnv();

  for (const source of selectedSources) {
    const result = await importSource(source, options);
    mergeStats(combinedStats, result.stats);
    combinedAnime.push(...result.generatedAnime);
  }

  const duplicateReport = buildDuplicateReport(combinedAnime);
  combinedStats.duplicateReport.push(...duplicateReport.lines);
  Object.assign(combinedStats, duplicateReport.counts);

  if (!options.dryRun && !options.listPlaylists) {
    writeJson(options.jsonOutput, combinedAnime);
    writeTypeScript(options.output, combinedAnime);
    combinedStats.outputFilesWritten.push(options.jsonOutput, options.output);
  }

  printSummary(combinedStats, options);
}

main().catch((error) => {
  console.error(`\nImport failed: ${error.message}`);
  process.exit(1);
});
